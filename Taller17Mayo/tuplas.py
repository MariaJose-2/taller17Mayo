#tuplas: cada elemento de una tupla puede ser de distinto tipo (punto flotante, entero, cadena, o cualquier otro tipo de dato).
numbers=(1,2,3,4,5,6,7,8) 
print(numbers)


#tupla tipo cadena:
domesticAnimals=("perro","gato","conejo","pajaros") #cuando es tipo cadena es necesario poner comillas ("")
print(domesticAnimals[3])#se le agrega corchetes para dar a elegir la opcion que quieres que aparezca en la consola([])

colors=("azul","rojo","rosado","verde","amarillo")
print(colors[0]) #cada valor se separa por comas(,)


#tupla de punto flotante:
floatingNumbers=(.1, .4, .43, 29.) #cuando es flotante se le agrega un punto y la consola al imprimirlo le agrega un cero dependiendo la ubicacion del punto flotante
print(floatingNumbers)
